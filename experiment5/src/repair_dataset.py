def _check_ai_keywords_fast(title: str, body: str, labels: list) -> bool:
    """快速 AI 关键词检查（在列表阶段使用，只查 title/body/labels）"""
    text = f"{title or ''} {body or ''} {' '.join(labels or [])}".lower()
    keywords = [
        "copilot", "chatgpt", "gpt", "claude", "cursor", "codex",
        "ai-generated", "ai generated", "generated by", "openai",
        "deepseek", "qwen", "code agent", "co-authored-by",
    ]
    for kw in keywords:
        if kw in text:
            return True
    # "ai" + context
    if "ai" in text:
        for ctx in ["generated", "agent", "copilot", "llm", "assisted", "powered"]:
            if ctx in text:
                return True
    return False


def collect_react_fallback(target: int = REACT_TARGET) -> List[Dict[str, Any]]:
    """
    绕过 Search API，用 Pulls List API 枚举 facebook/react 的 closed PR，
    筛选包含 AI 关键词的候选，再采集完整数据。

    参数:
        target: 目标 AI PR 数量

    返回:
        List[dict]: 采集的 PR 数据（与 collect_single_pr 同格式）
    """
    logger.info("=" * 60)
    logger.info(f"修复: facebook/react 补充采集 (目标: {target})")
    logger.info("=" * 60)

    # Step 1: 枚举 closed PR，筛选 AI 关键词
    logger.info("枚举 facebook/react closed PRs...")
    candidates = []
    pages = 0
    max_pages = 50  # 最多扫 5000 条 PR
    per_page = 100

    for page in range(1, max_pages + 1):
        status, data, error = github_api_request(
            f"/repos/{REACT_REPO}/pulls",
            params={
                "state": "closed",
                "sort": "created",
                "direction": "desc",
                "per_page": per_page,
                "page": page,
            },
        )
        if error:
            logger.warning(f"  page={page} 失败: {error}")
            break

        if not data or not isinstance(data, list):
            break

        pages += 1
        for pr in data:
            if not isinstance(pr, dict):
                continue
            title = pr.get("title") or ""
            body = pr.get("body") or ""
            labels = [lbl.get("name", "") for lbl in (pr.get("labels") or [])]

            if _check_ai_keywords_fast(title, body, labels):
                candidates.append({
                    "repo": REACT_REPO,
                    "number": pr.get("number"),
                    "title": title,
                    "user_login": (pr.get("user") or {}).get("login", ""),
                    "created_at": pr.get("created_at", ""),
                })

        logger.info(
            f"  已扫 {pages * per_page} PRs, 候选: {len(candidates)}"
        )

        if len(candidates) >= target * 2:
            # 有足够候选了
            break

        if len(data) < per_page:
            break

    logger.info(f"共扫描 {pages * per_page} PRs, 找到 {len(candidates)} AI 候选")

    if not candidates:
        logger.warning("未找到任何 AI 候选 PR")
        return []

    # Step 2: 排除 seed 中已有的
    seed_keys = _load_existing_pr_keys()
    new_candidates = [
        c for c in candidates
        if (REACT_REPO, c["number"]) not in seed_keys
    ]
    logger.info(f"排除 seed 重复后: {len(new_candidates)} 候选")

    # 限制候选数量（取 target * 1.5 以确保最终有足够通过 AI 检测的）
    if len(new_candidates) > target * 2:
        random.shuffle(new_candidates)
        new_candidates = new_candidates[:target * 2]

    # Step 3: 多线程采集完整数据
    logger.info(f"多线程采集 {len(new_candidates)} 个 react PR...")
    collected = {}
    failed = 0

    # 复用 ai_data_collector 中的采集逻辑
    from ai_data_collector import collect_single_pr

    with ThreadPoolExecutor(max_workers=MAX_WORKERS) as executor:
        future_to_c = {}
        for c in new_candidates:
            future = executor.submit(
                collect_single_pr, REACT_REPO, c["number"], c
            )
            future_to_c[future] = c

        for i, future in enumerate(as_completed(future_to_c)):
            c = future_to_c[future]
            try:
                result = future.result(timeout=120)
                if result:
                    # AI 检测
                    from ai_data_collector import run_ai_detection_on_collected
                    has_ai, reason = run_ai_detection_on_collected(result)
                    if has_ai:
                        result["_ai_detection"] = {
                            "has_ai_generated_code": True,
                            "ai_detection_reason": reason,
                        }
                        key = f"{REACT_REPO}#{c['number']}"
                        collected[key] = result
                    else:
                        failed += 1
                else:
                    failed += 1
            except Exception as e:
                logger.warning(f"  采集失败 react#{c['number']}: {e}")
                failed += 1

            if (i + 1) % 20 == 0:
                logger.info(f"  进度: {i+1}/{len(new_candidates)} (成功: {len(collected)}, 失败: {failed})")

    logger.info(f"react 采集完成: {len(collected)} 成功, {failed} 失败")

    # Step 4: 限制到 target
    result_list = list(collected.values())
    if len(result_list) > target:
        random.shuffle(result_list)
        result_list = result_list[:target]

    return result_list


def _load_existing_pr_keys() -> set:
    """加载已有数据集中所有 (repo, pr_number)"""
    keys = set()
    # 从 ai_pr_collected.json 加载
    if os.path.exists(AI_COLLECTED_JSON):
        data = _load_json_safe(AI_COLLECTED_JSON)
        if data:
            for pr in data.get("ai_positive_prs", []):
                keys.add((pr.get("repo", ""), int(pr.get("pr_number", 0))))
            for pr in data.get("prs", []):
                keys.add((pr.get("repo", ""), int(pr.get("pr_number", 0))))

    # 从 checkpoint 加载
    if os.path.exists(AI_COLLECTED_CHECKPOINT):
        ckpt = _load_json_safe(AI_COLLECTED_CHECKPOINT)
        if ckpt:
            for key_str in ckpt:
                if "#" in key_str:
                    repo, num = key_str.rsplit("#", 1)
                    try:
                        keys.add((repo, int(num)))
                    except ValueError:
                        pass

    # 从 seed 加载
    seed_csv = os.path.join(RESULTS_PROCESSED_DIR, "ai_generated_dataset_seed.csv")
    if os.path.exists(seed_csv):
        with open(seed_csv, "r", encoding="utf-8-sig") as f:
            for row in csv.DictReader(f):
                keys.add((row.get("repo", ""), int(row.get("pr_number", 0))))

    return keys


# ============================================================
# Part 2: vscode 降采样
# ============================================================
def downsample_vscode(prs: List[Dict], seed_keys: set, cap: int = VSCODE_NEW_CAP) -> List[Dict]:
    """
    对 vscode 的新采集 PR 降采样。

    参数:
        prs: ai_positive_prs 列表
        seed_keys: seed 中已有的 (repo, pr_number) 集合
        cap: vscode 新增上限

    返回:
        List[dict]: 降采样后的 prs 列表
    """
    vscode_new = []
    vscode_seed = []
    other_prs = []

    for pr in prs:
        repo = pr.get("repo", "")
        key = (repo, int(pr.get("pr_number", 0)))
        if repo == VSCODE_REPO:
            if key in seed_keys:
                vscode_seed.append(pr)
            else:
                vscode_new.append(pr)
        else:
            other_prs.append(pr)

    logger.info(f"vscode 降采样前: seed={len(vscode_seed)}, new={len(vscode_new)}")

    if len(vscode_new) > cap:
        random.shuffle(vscode_new)
        vscode_new = vscode_new[:cap]

    logger.info(f"vscode 降采样后: seed={len(vscode_seed)}, new={len(vscode_new)}")

    return other_prs + vscode_seed + vscode_new


