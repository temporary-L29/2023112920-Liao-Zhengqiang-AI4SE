# Part 3: 重建最终数据集
# ============================================================
def rebuild_final_dataset(react_new: List[Dict], all_prs: List[Dict]):
    """用修复后的数据重建最终数据集"""
    from merge_ai_datasets import (
        _read_csv_robust,
        _pr_to_csv_row,
        _build_merged_patch_index,
        compute_dataset_stats,
    )
    from config import AI_DATASET_SEED_CSV

    logger.info("=" * 60)
    logger.info("重建最终数据集")
    logger.info("=" * 60)

    # 1. 提取 react PR 为结构化数据
    react_extracted = []
    for raw in react_new:
        detail = raw.get("detail", {})
        files = raw.get("files", [])
        commits = raw.get("commits", [])
        reviews = raw.get("reviews", [])
        review_comments = raw.get("review_comments", [])
        ai_info = raw.get("_ai_detection", {})
        ai_reason = ai_info.get("ai_detection_reason", "")

        pr_data = extract_pr_data(
            detail, files, commits, reviews, review_comments,
            ai_detection_reason=ai_reason,
        )
        pr_data["_raw_ref"] = f"{REACT_REPO}#{detail.get('number', '')}"
        pr_data["_files"] = files
        react_extracted.append(pr_data)

    logger.info(f"react 新增结构化数据: {len(react_extracted)} 条")

    # 2. 合并所有 PR（修复后的 vscode + 其他 + react 新增）
    all_extracted = list(all_prs)  # 已经是降采样后的
    all_extracted.extend(react_extracted)

    # 去重
    seen = set()
    unique = []
    for pr in all_extracted:
        key = (pr.get("repo", ""), str(pr.get("pr_number", "")))
        if key not in seen:
            seen.add(key)
            unique.append(pr)

    logger.info(f"合并后总 PR: {len(unique)}")

    # 3. 更新 collected JSON
    collected_data = _load_json_safe(AI_COLLECTED_JSON) or {}
    collected_data["repaired_at"] = datetime.now(timezone.utc).isoformat()
    collected_data["total_ai_positive"] = len(unique)
    collected_data["ai_positive_prs"] = unique
    collected_data["react_new_count"] = len(react_extracted)
    collected_data["vscode_downsampled"] = True
    _save_json_safe(AI_COLLECTED_JSON, collected_data)

    # 4. 保存 enhanced CSV
    seed_rows = _read_csv_robust(AI_DATASET_SEED_CSV)
    seed_fieldnames = list(seed_rows[0].keys()) if seed_rows else list(unique[0].keys())

    all_enhanced = list(seed_rows)
    for pr in unique:
        # 跳过 seed 已有的
        key = (pr.get("repo", ""), int(pr.get("pr_number", 0)))
        seed_keys_set = {
            (r.get("repo", ""), int(r.get("pr_number", 0)))
            for r in seed_rows
        }
        if key not in seed_keys_set:
            row = _pr_to_csv_row(pr, seed_fieldnames)
            all_enhanced.append(row)

    with open(AI_DATASET_ENHANCED_CSV, "w", encoding="utf-8-sig", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=seed_fieldnames, extrasaction="ignore")
        writer.writeheader()
        writer.writerows(all_enhanced)
    logger.info(f"Enhanced CSV: {len(all_enhanced)} 行 ({AI_DATASET_ENHANCED_CSV})")

    # 5. 保存最终 CSV
    with open(AI_DATASET_CSV, "w", encoding="utf-8-sig", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=seed_fieldnames, extrasaction="ignore")
        writer.writeheader()
        writer.writerows(all_enhanced)
    logger.info(f"最终 CSV: {len(all_enhanced)} 行 ({AI_DATASET_CSV})")

    # 6. 重建 patch index
    seed_index = {}
    seed_patch_path = os.path.join(RESULTS_PROCESSED_DIR, "ai_patch_index_seed.json")
    if os.path.exists(seed_patch_path):
        seed_index = _load_json_safe(seed_patch_path) or {}

    merged_index = _build_merged_patch_index(seed_index, unique)
    with open(AI_PATCH_INDEX, "w", encoding="utf-8") as f:
        json.dump(merged_index, f, ensure_ascii=False, indent=2)
    logger.info(f"Patch index: {len(merged_index)} 条目")

    # 7. 统计报告
    stats = compute_dataset_stats(
        [dict(r) if isinstance(r, dict) else {} for r in all_enhanced],
        dataset_type="repaired",
        source_desc=f"enhanced + react补{len(react_extracted)}条 + vscode降采样",
    )
    stats["version"] = "repaired"
    stats["react_new"] = len(react_extracted)
    stats["vscode_downsampled"] = True

    stats_path = os.path.join(RESULTS_EVALUATION_DIR, "ai_dataset_stats.json")
    with open(stats_path, "w", encoding="utf-8") as f:
        json.dump(stats, f, ensure_ascii=False, indent=2)
    logger.info(f"统计报告: {stats_path}")

    # 打印摘要
    logger.info("=" * 60)
    logger.info("数据集修复完成")
    logger.info(f"  总样本:           {stats['total_prs']}")
    logger.info(f"  react 新增:       {len(react_extracted)}")
    logger.info(f"  vscode 降采样:    是")
    logger.info(f"  合并率:           {stats['merge_rate']:.2%}")
    logger.info(f"  评论覆盖:         {stats['review_coverage']:.2%}")
    logger.info("  仓库分布:")
    for repo, dist in sorted(stats["repo_distribution"].items(), key=lambda x: -x[1]["total"]):
        logger.info(f"    {repo}: {dist['total']} (m={dist['merged']}, u={dist['unmerged']})")
    logger.info("=" * 60)


def main():
    logger.info("=" * 60)
    logger.info("数据集修复: react 补充 + vscode 降采样")
    logger.info("=" * 60)

    # 1. 加载当前数据
    collected_data = _load_json_safe(AI_COLLECTED_JSON)
    if not collected_data:
        logger.error("未找到采集数据，请先运行 --collect-ai")
        return False

    all_prs = collected_data.get("ai_positive_prs", [])
    logger.info(f"当前 AI positive PRs: {len(all_prs)}")

    # 2. 获取 seed keys
    seed_keys = _load_existing_pr_keys()
    # 只保留 seed CSV 中的 keys
    seed_csv = os.path.join(RESULTS_PROCESSED_DIR, "ai_generated_dataset_seed.csv")
    seed_only_keys = set()
    if os.path.exists(seed_csv):
        with open(seed_csv, "r", encoding="utf-8-sig") as f:
            for row in csv.DictReader(f):
                seed_only_keys.add((row.get("repo", ""), int(row.get("pr_number", 0))))

    # 3. vscode 降采样
    logger.info("\n--- vscode 降采样 ---")
    all_prs = downsample_vscode(all_prs, seed_only_keys, cap=VSCODE_NEW_CAP)

    # 4. react 补充采集
    logger.info("\n--- react 补充采集 ---")
    react_new = collect_react_fallback(target=REACT_TARGET)

    # 5. 重建
    rebuild_final_dataset(react_new, all_prs)

    return True


if __name__ == "__main__":
    success = main()
    sys.exit(0 if success else 1)
